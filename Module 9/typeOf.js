// typeof Operator :

let a = 2
console.log(typeof (a)) // number

let b = 'scalar'
console.log(typeof (b)) // string

let c = true
console.log(typeof (c)) // boolean

let myObj = {
  name: 'onkar',
  age: 24
}

console.log(typeof (myObj)) // object

let arr = [1, 2, 3, 4]
console.log(typeof (arr)) // object
// Always remember that in javascript arrays are objects, so it always been return a object
